--[[
Item images for dota2 guides using pictures hosted on dota2alttab.com.
]]

local Items = {}

local base_items = require "data/base/items"

local function I(id, img, data)
  assert(base_items[id], "Unknown item "..id)
  
  data = data or {}
  data.image = img
  
  Items[id] = data
end

local function p(name)
  return string.format(
    "./Dota2_Alt_Tab/Images/Items/%s.png",
    name
  )
end

